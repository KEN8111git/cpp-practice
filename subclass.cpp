#include "subclass.hpp"

int calculation::add(int a_num1, int a_num2)
{
  return (a_num1 + a_num2);
}

int calculation::subtract(int s_num1, int s_num2)
{
  return (s_num1 - s_num2);
}
int calculation::multiple(int m_num1, int m_num2)
{
  return (m_num1 * m_num2);
}
int calculation::division(int d_num1, int d_num2)
{
  return (d_num1 / d_num2);
}
